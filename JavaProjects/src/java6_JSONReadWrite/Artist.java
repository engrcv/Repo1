package java6_JSONReadWrite;

public class Artist {

	public String Name;
	public Integer ID;

	public void setId(Integer ID) {
		this.ID = ID;
	}

	public void setName(String Name) {
		this.Name = Name;
	}

	public Integer getId() {
		return ID;
	}

	public String getName() {
		return Name;
	}
}
